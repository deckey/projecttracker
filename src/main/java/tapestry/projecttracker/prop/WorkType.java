package tapestry.projecttracker.prop;

/**
 * Different types of work members can do
 *
 * @author Dejan Ivanovic divanovic3d@gmail.com
 */
public enum WorkType {

    /**
     * Illustration work, drawing
     */
    Illustration,

    /**
     * Concept art, drafting, design
     */
    Concept,

    /**
     * Storyboard art
     */
    Storyboard,

    /**
     * 3d animation 
     */
    Animation,

    /**
     * Technical animation setup
     */
    Rigging,

    /**
     * Modeling and texturing
     */
    Modeling,

    /** 
     * Lighting and materials, look development
     */
    LookDevelopment,

    /**
     * Rendering 
     */
    Rendering,

    /**
     * 2D and 3D compositing
     */
    Compositing,

    /**
     * Video editing, montage
     */
    VideoEditing,

    /**
     * Sound effects edit
     */
    SoundFX,

    /**
     * Visual effects, dynamics, smoke, hair etc.
     */
    VisualFX;
}
